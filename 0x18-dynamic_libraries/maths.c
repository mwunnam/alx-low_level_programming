#include <stdio.h>
#include "main.h"

/**
 * add - Function that add to numbers
 * @num1: first number
 * @num2: second number
 * Return: num1 + num2
 **/
int add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 * sub - Function that add to numbers
 * @num1: first number
 * @num2: second number
 * Return: num1 - num2
 **/
int sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * mul - Function that add to numbers
 * @num1: first number
 * @num2: second number
 * Return: num1 * num2
 **/
int mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * mod - Function that add to numbers
 * @num1: first number
 * @num2: second number
 * Return: num1 % num2
 **/
int mod(int num1, int num2)
{
	return (num1 % num2);
}

/**
 * div - Function that add to numbers
 * @num1: first number
 * @num2: second number
 * Return: num1 % num2
 **/
int div(int num1, int num2)
{
	return (num1 / num2);
}

